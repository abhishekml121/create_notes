  if (count($diff2) == 0 && (count($GET_keys) == count($required_KEYS))) {
    return true;
}
return false;
}

// we are currently accepting only word characters for paswd.
function is_valid_paswd($paswd, $pattern = '/[\w]+/'){
    if(has_presence($paswd)){
      preg_match($pattern, $paswd, $matched);
      if(!empty($matched) && strlen($matched[0]) == strlen($paswd)){
        return true;
      }
    }
    return false;
}

